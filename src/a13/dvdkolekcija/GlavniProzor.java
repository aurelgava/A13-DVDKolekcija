    private void populateTable() {
        try {
            //Popunjavanje tabele
            Statement s = c.createStatement();
            ResultSet rs = s.executeQuery("SELECT * FROM Producent");
            DefaultTableModel dtm = new DefaultTableModel();
            dtm.addColumn("Sifra");
            dtm.addColumn("Naziv producentske kuce");
            dtm.addColumn("E-mail adresa");
            while(rs.next()){
                Object[] red = new Object[3];
                red[0] = rs.getInt("ProducentID");
                red[1] = rs.getString("Ime");
                red[2] = rs.getURL("Email");
                dtm.addRow(red);
            }
            jTable1.setModel(dtm);
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Greska prilikom citanja iz baze", "Greska!", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(GlavniProzor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void populateTextFields(){
        //Popunjavanje textField-ova sa podacima koji imaju najmanji ID
        try{
            Statement s1 = c.createStatement();
            ResultSet rs1 = s1.executeQuery(" SELECT * FROM Producent WHERE ProducentID = (SELECT MIN(ProducentID) FROM Producent)");
            if(rs1.next()){
                jTextField1.setText(Integer.toString(rs1.getInt("ProducentID")));
                jTextField2.setText(rs1.getString("Ime"));
                jTextField3.setText(rs1.getURL("Email").toString());
            }else{
                JOptionPane.showMessageDialog(this, "Greska prilikom citanja iz baze", "Greska!", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Greska prilikom citanja iz baze", "Greska!", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(GlavniProzor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
